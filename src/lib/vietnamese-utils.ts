/**
 * Converts Vietnamese text to non-accented text
 * @param text - The Vietnamese text to convert
 * @returns The converted text without diacritics
 */
export function removeVietnameseDiacritics(text: string): string {
  return text.split('').map(char => vietnameseMap[char] || char).join('');
}

/**
 * Generates a URL-friendly slug from Vietnamese text
 * @param text - The text to convert to slug
 * @returns A URL-friendly slug
 */
export function generateVietnameseSlug(text: string): string {
  if (!text) return '';
  
  // Remove Vietnamese diacritics
  let slug = removeVietnameseDiacritics(text);
  
  // Convert to lowercase
  slug = slug.toLowerCase();
  
  // Replace spaces and special characters with hyphens
  slug = slug.replace(/[^a-z0-9\s-]/g, '');
  
  // Replace multiple spaces with single hyphen
  slug = slug.replace(/\s+/g, '-');
  
  // Replace multiple hyphens with single hyphen
  slug = slug.replace(/-+/g, '-');
  
  // Remove leading and trailing hyphens
  slug = slug.replace(/^-+|-+$/g, '');
  
  return slug;
}

/**
 * Checks if a slug is valid (contains only allowed characters)
 * @param slug - The slug to validate
 * @returns True if the slug is valid
 */
export function isValidSlug(slug: string): boolean {
  return /^[a-z0-9-]+$/.test(slug);
}

/**
 * Formats a date to Vietnamese format
 * @param date - The date to format (Date object or date string)
 * @returns Formatted date string in Vietnamese format (dd/mm/yyyy)
 */
export function formatVietnameseDate(date: Date | string): string {
  const dateObj = typeof date === 'string' ? new Date(date) : date;
  
  if (isNaN(dateObj.getTime())) {
    return '';
  }
  
  const day = dateObj.getDate().toString().padStart(2, '0');
  const month = (dateObj.getMonth() + 1).toString().padStart(2, '0');
  const year = dateObj.getFullYear();
  
  return `${day}/${month}/${year}`;
}

/**
 * Formats a date to Vietnamese relative time (e.g., "2 ngày trước", "1 tuần trước")
 * @param date - The date to format
 * @returns Formatted relative time string in Vietnamese
 */
export function formatVietnameseRelativeTime(date: Date | string): string {
  const dateObj = typeof date === 'string' ? new Date(date) : date;
  const now = new Date();
  const diffInMs = now.getTime() - dateObj.getTime();
  const diffInDays = Math.floor(diffInMs / (1000 * 60 * 60 * 24));
  
  if (diffInDays === 0) {
    return 'Hôm nay';
  } else if (diffInDays === 1) {
    return 'Hôm qua';
  } else if (diffInDays < 7) {
    return `${diffInDays} ngày trước`;
  } else if (diffInDays < 30) {
    const weeks = Math.floor(diffInDays / 7);
    return `${weeks} tuần trước`;
  } else if (diffInDays < 365) {
    const months = Math.floor(diffInDays / 30);
    return `${months} tháng trước`;
  } else {
    const years = Math.floor(diffInDays / 365);
    return `${years} năm trước`;
  }
} 